#Escreva um algoritmo em Python que recebe dois números inteiros e exibe: 
#a soma desses dois números, a multiplicação, a divisão inteira e o resto da divisão inteira.

valor = input('Digite um número: ')
numA = int(valor)

valor = input('Digite outro número: ')
numB = int(valor)

print('A soma dos números é: ', numA + numB)
print('A multiplicação dos números é: ', numA * numB)
print('A divisão inteira dos números é: ', numA // numB)
print('O resto da divisão inteira dos números é: ', numA % numB)

